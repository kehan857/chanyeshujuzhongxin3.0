import { computed } from 'vue'
import { UserFilled, MuteNotification, Top } from '@element-plus/icons-vue'
import { formatDistanceToNow } from 'date-fns'
import { zhCN } from 'date-fns/locale'

import TypingAnimation from '@/components/common/TypingAnimation.vue'

export default {
  name: 'ConversationItem',
  components: {
    UserFilled,
    MuteNotification,
    Top,
    TypingAnimation
  },
  props: {
    conversation: {
      type: Object,
      required: true
    },
    active: {
      type: Boolean,
      default: false
    }
  },
  emits: ['click', 'context-menu'],

  setup(props, { emit }) {
    // ËÆ°ÁÆóÂ±ûÊÄß
    const avatarText = computed(() => {
      return getAvatarText(props.conversation.name)
    })

    // ÊñπÊ≥ï
    const handleClick = () => {
      emit('click', props.conversation)
    }

    const handleContextMenu = (event) => {
      emit('context-menu', event, props.conversation)
    }

    const getAvatarText = (name) => {
      if (!name) return '?'
      // Âèñ‰∏≠ÊñáÂêçÂ≠óÁöÑÊúÄÂêé‰∏Ä‰∏™Â≠óÔºåÊàñËã±ÊñáÂêçÁöÑÈ¶ñÂ≠óÊØç
      const chars = name.trim()
      if (/[\u4e00-\u9fa5]/.test(chars)) {
        return chars.slice(-1)
      } else {
        return chars.charAt(0).toUpperCase()
      }
    }

    const formatTime = (time) => {
      if (!time) return ''
      
      const date = new Date(time)
      const now = new Date()
      const diffInHours = (now - date) / (1000 * 60 * 60)

      if (diffInHours < 24) {
        // 24Â∞èÊó∂ÂÜÖÊòæÁ§∫Êó∂Èó¥
        return date.toLocaleTimeString('zh-CN', {
          hour: '2-digit',
          minute: '2-digit'
        })
      } else if (diffInHours < 24 * 7) {
        // ‰∏ÄÂë®ÂÜÖÊòæÁ§∫ÊòüÊúü
        return date.toLocaleDateString('zh-CN', { weekday: 'short' })
      } else {
        // Ë∂ÖËøá‰∏ÄÂë®ÊòæÁ§∫Êó•Êúü
        return date.toLocaleDateString('zh-CN', {
          month: 'short',
          day: 'numeric'
        })
      }
    }

    const getSenderDisplayName = (sender) => {
      if (sender === 'self') return 'Êàë'
      if (sender === 'ai') return 'AI'
      return sender || 'ÂØπÊñπ'
    }

    const getMessagePreview = (message) => {
      if (!message) return 'ÊöÇÊó†Ê∂àÊÅØ'

      // Â§ÑÁêÜ‰∏çÂêåÁ±ªÂûãÁöÑÊ∂àÊÅØ
      if (typeof message === 'object') {
        switch (message.type) {
          case 'image':
            return '[ÂõæÁâá]'
          case 'file':
            return '[Êñá‰ª∂]'
          case 'voice':
            return '[ËØ≠Èü≥]'
          case 'video':
            return '[ËßÜÈ¢ë]'
          case 'miniprogram':
            return '[Â∞èÁ®ãÂ∫è]'
          default:
            return message.content || '[Ê∂àÊÅØ]'
        }
      }

      // ÊñáÊú¨Ê∂àÊÅØÊà™Êñ≠Â§ÑÁêÜ
      const text = String(message).replace(/\n/g, ' ')
      return text.length > 30 ? text.substring(0, 30) + '...' : text
    }

    const getTagType = (tag) => {
      const tagTypeMap = {
        'VIPÂÆ¢Êà∑': 'warning',
        'È´òÊÑèÂêë': 'success',
        'ÊΩúÂú®ÂÆ¢Êà∑': 'info',
        'Â∑≤‰ªòË¥π': 'success',
        'ÈúÄË∑üËøõ': 'danger'
      }
      return tagTypeMap[tag] || 'info'
    }

    return {
      avatarText,
      handleClick,
      handleContextMenu,
      getAvatarText,
      formatTime,
      getSenderDisplayName,
      getMessagePreview,
      getTagType
    }
  }
}
</script>

